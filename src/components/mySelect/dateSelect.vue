<template>
  <div class="inputBox">
      <label @click="openDatePicker">
          <span>{{label}}<span class="yan-red" v-show="required">*</span></span>
          <input  type="text" :value="value" readonly  :placeholder="placeholder" @input="$emit('input',$event.target.value)">
      </label>
      <div class="icon-right" v-if="showIcon">
        <img src="../../assets/imgs/rightside.png" alt="">
      </div>
      <mt-datetime-picker
        v-if="day==='end'"
        v-model="pickerVisible"
        type="date"
        :endDate="new Date()"
        year-format="{value}年"
        month-format="{value}月"
        date-format="{value}日"
        @confirm="handleConfirm"
        ref="date">
      </mt-datetime-picker>
      <mt-datetime-picker
        v-else
        v-model="pickerVisible"
        type="date"
        :startDate="new Date()"
        year-format="{value}年"
        month-format="{value}月"
        date-format="{value}日"
        @confirm="handleConfirm"
        ref="date">
      </mt-datetime-picker>
   </div>
</template>

<script>
import Vue from 'vue'
import { DatetimePicker } from 'mint-ui';
Vue.component(DatetimePicker.name, DatetimePicker);
import mango from '../../js/'
export default {
  props:['value','label','placeholder','showIcon','required','day'],
  data(){
    return{
      pickerVisible:new Date()
    }
  },
  methods:{
    handleConfirm(){
      let day=mango.indexTimeB(this.pickerVisible)[0];
      let anotherDay=mango.indexTimeB(this.pickerVisible)[1];
      console.log(day)
      console.log(anotherDay);
      this.$emit('update',day,anotherDay);
    },
    openDatePicker(){
      if(this.showIcon){
        this.$refs.date.open();
      }else{
        return;
      }
      
    }
  }
 
}
</script>

