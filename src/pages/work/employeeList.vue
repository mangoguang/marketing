<template>
    <div class="employee">
      <mybanner title="员工" style="background:#fff;border:none" ref='banner'></mybanner>
      <search-input ref='search' v-model.trim="key" placeholder="请输入员工名称" @input="search" :style="{marginTop:`${top}vw`}"></search-input>
      <div  v-if="list.length>0">
        <ul v-for="(item,index) in list" :key="index" @click="go(item.id,item.username)">
          <li is="employeeLi"  :icon="true">
            <div class="headPortrait" slot="headPortrait">
              <img :src="img" alt="">
            </div>
            <div class="detail">
              <h1>{{item.username}}</h1>
              <span>{{item.shopName}}</span>
              <p>意向客户数：<b>{{item.countOpport}}</b></p>
            </div>
          </li>
        </ul>
      </div>
      <div class="noData" v-else>{{status}}</div>
    </div>
</template>
<script>
import Vue from 'vue'
import { Toast,Loadmore } from 'mint-ui'
Vue.component(Loadmore.name, Loadmore)
import mybanner from '../../components/banner'
import searchInput from '../../components/search/searchInput'
import employeeLi from '../../components/work/employee/employeeLi'
import { mapState, mapMutations } from 'vuex'
import mango from '../../js'
import { IndexModel } from '../../utils'
import { Debounce } from '../../utils/public'
const indexModel=new IndexModel()
export default {
  data () {
    return {
      key:'',
      top:'',
      status:'',
      img:'./static/images/avatar.png'
    }
  },
  components:{
     mybanner,
     searchInput,
     employeeLi
  },
  computed:{
    ...mapState({
      list:state => state.employeeList.list
    })
  }, 
  created(){
    this.getData({username:''})
  },
  
  mounted(){
      this.isIPhoneX();
  },
  methods:{
    ...mapMutations('employeeList',['setList']),
   isIPhoneX(){
      let phone=this.phoneSize()
      if(phone==="iphonex"){
        this.top='6'
      }else{
        this.top='0'
       
      }
    },
   search(){
    var that=this;
      Debounce(function(){
       that.getData({username:that.key})
      },500)()
    
   },
   getData(obj){
     indexModel.getEmployeeList(obj).then((res) => {
       console.log(res);
       if(res.status==1){
          this.setList(res.data)
         if(res.data.length>0){
            this.status=''
         }else{
           this.status='暂无记录'
         }
        
       }else{
         toast(res.msg)
       }
     }).catch((reject) => {
       if(reject === 510){
         this.getData(obj)
       }
     })
   },
   go(userId,userName){
     this.$router.push({name:'employeeDetail',params:{id:userId,name:userName}})
   }
  }
};
</script>

