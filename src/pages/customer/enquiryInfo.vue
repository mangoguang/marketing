<script>
import { MessageBox } from 'mint-ui'
import { IndexModel } from '../../utils/index'
import variable from '../../js/variable'
const indexModel = new IndexModel()
import dealHeader from '../../components/customer/dealCustomer/dealHeader'
import newDescript from '../../components/customer/newCustomerInfo/newDescript'
import OrderInfo from '../../components/customer/dealCustomer/orderInfo'
import CustomerMsg from '../../components/customer/customerShare/customerMsg'
import intentionMsg from '../../components/customer/customerShare/intentionMsg'
import btn from '../../components/btn'
import Vuex, { mapMutations, mapState } from 'vuex'
import mango from '../../js'
import EnquiryOrderInfo from '../../components/customer/enquiryOrder/enquiryOrderInfo'
import orderInfoDetails from '../../components/customer/dealCustomer/orderInfoDetails'
import { explainType } from '../../utils/customer'
import { btnList } from '../../utils/gallery'
export default {
  components: {
    EnquiryOrderInfo,
    orderInfoDetails,
    dealHeader,
    OrderInfo,
    CustomerMsg,
    newDescript,
    btn,
    intentionMsg
  },
  data() {
    return {
      list: {},
      type: [],
      editStatus: false,
      shops: '',
      shpoId: '',
      index: '',
      phone: '',
      wechat: '',
      edit: ''
    }
  },
  computed: {
    ...mapState({
      orderInfoDetails: state => state.orderInfoDetails.orderInfoDetails,
      dealTabStatus: state => state.tabStatus.dealTabStatus,
      newCustomerInfo: state => state.customer.newCustomerInfo,
      upLoadUrl: state => state.loadImgUrl.upLoadUrl,
      descriptShopVal: state => state.chooseShop.descriptShopVal
    })
  },
  beforeRouteEnter(to, from, next) {
    next(vm => {
      if (from.name === 'selectAddress') {
        vm.editStatus = true
      } else {
        vm.setUpLoadUrl('')
      }
    })
  },
  created() {
    this.getStatus()
    this.getData()
  },
  destroyed() {
    this.setUpLoadUrl('')
  },
  methods: {
    ...mapMutations([
      'getDescriptShopVal',
      'initDescriptShopList',
      'setDealTabStatus',
      'setUpLoadUrl',
      'setNewCustomerInfo'
    ]),
    getData() {
      //从父级传id过来请求
      indexModel
        .getCustomerDetails(this.$route.query.id)
        .then(res => {
          if (res.data) {
            this.list = res.data
            let arr = this.changeStatus(this.list.orderList)
            this.list.orderList = arr
            this.getShopName(this.list.orgId)
            this.phone = res.data.phone === '0' ? '' : res.data.phone
            this.wechat = res.data.weChat
          }
        })
        .catch(reject => {
          if (reject === 510) {
            this.getData()
          }
        })
    },
    //获取status的type数组
    getStatus() {
      this.getType('FS_ORDER_STATUS')
      setTimeout(() => {
        this.type = this._type
      }, 100)
    },
    //更改status
    changeStatus(arr) {
      arr.map(item => {
        item.status = explainType(this.type, item.status)
      })
      return arr
    },
    //编辑按钮
    editMsg(val) {
      this.editStatus = val
    },
    //保存资料
    async saveMsg() {
      if (!this.newCustomerInfo.sex) {
        MessageBox.alert('性别不能为空')
        return
      }
      if (!this.newCustomerInfo.username) {
        MessageBox.alert('称呼不能为空')
        return
      }
      let testName = /^[\u4e00-\u9fa5]{2,}$/
      if (!testName.test(this.newCustomerInfo.username)) {
        MessageBox.alert('称呼只能输入中文且不能少于2个字')
        return
      }

      let passName = await this.recycleName(this.newCustomerInfo.username)
      console.log(passName, 'ss')
      if (!passName) {
        MessageBox.alert('姓氏不存在')
        return
      }
      let testPhone = variable.testPhone(this.newCustomerInfo.phone)
      if (
        (!this.newCustomerInfo.phone || this.newCustomerInfo.phone === '') &&
        !this.newCustomerInfo.weChat
      ) {
        MessageBox.alert('请填写手机号码或微信号')
        return
      }
      if (!this.newCustomerInfo.phone || this.newCustomerInfo.phone === '') {
        if (this.phone !== '' && this.newCustomerInfo.phone === '') {
          MessageBox.alert('手机号码不能修改为空')
          return
        }
        this.wx(this.newCustomerInfo.weChat)
      }
      if (
        this.newCustomerInfo.phone &&
        this.phone !== this.newCustomerInfo.phone
      ) {
        let ishasPhone
        if (!testPhone) {
          MessageBox.alert('请填写正确手机号码')
          return
        } else {
          ishasPhone = await this.checkPhone(
            'phone',
            this.newCustomerInfo.phone
          )
        }
        if (ishasPhone) {
          return
        }
        this.wx(this.newCustomerInfo.weChat)
      }
      if (
        this.newCustomerInfo.phone &&
        this.phone === this.newCustomerInfo.phone
      ) {
        this.wx(this.newCustomerInfo.weChat)
      }
    },
    //check
    async wx(wechat) {
      let testWeChat = variable.testWeChat(wechat)
      let ishasWeChat
      if (this.wechat && !wechat) {
        MessageBox.alert('微信号不能修改为空')
        return
      }
      if (wechat && this.wechat !== wechat) {
        if (!testWeChat) {
          MessageBox.alert('请填写正确微信号')
          return
        } else {
          ishasWeChat = await this.checkPhone('wechat', wechat)
          console.log(ishasWeChat)
        }
      } else if (wechat && this.wechat === wechat) {
        ishasWeChat = false
      } else {
        ishasWeChat = false
      }
      if (ishasWeChat) {
        return
      }
      let check
      if (this.newCustomerInfo.qq && this.newCustomerInfo.qq !== '') {
        check = await this.checkQQ()
      } else {
        check = true
      }
      if (!check) {
        return
      }
      //@#$%^&*(){}<>
      let dutyReg = /^[^\@\#\$\%\^\&\*\(\)\{\}\<\>]{1,}$/
      if (
        this.newCustomerInfo.duty !== '' &&
        !dutyReg.test(this.newCustomerInfo.duty)
      ) {
        MessageBox.alert('客户职业不能输入特殊字符及表情符号')
        return
      }
      let remarkReg = /[\ud800-\udbff][\udc00-\udfff]/g
      let reg = /^[^\@\#\$\%\^\&\*\(\)\{\}\<\>]{1,}$/
      if (
        this.newCustomerInfo.remark !== '' &&
        remarkReg.test(this.newCustomerInfo.remark)
      ) {
        this.newCustomerInfo.remark = this.newCustomerInfo.remark.replace(
          /[\ud800-\udbff][\udc00-\udfff]/g,
          ''
        )
        MessageBox.alert('客户描述不能输入特殊字符及表情符号')
        return
      }
      if (
        this.newCustomerInfo.remark !== '' &&
        !reg.test(this.newCustomerInfo.remark)
      ) {
        MessageBox.alert('客户描述不能输入特殊字符及表情符号')
        return
      }
      this.saveData()
    },
    async checkQQ() {
      let check
      let testQQ = /^[1-9][0-9]{4,9}$/
      if (!testQQ.test(this.newCustomerInfo.qq)) {
        MessageBox.alert('请输入正确的QQ')
        check = false
        return
      } else {
        check = true
      }
      return check
    },
    async checkName(name) {
      let isExist
      await indexModel
        .checkLastName({ lastName: name })
        .then(res => {
          console.log('检查名字', res)
          if (res.data) {
            isExist = true
          } else {
            isExist = false
          }
        })
        .catch(reject => {
          if (reject === 510) {
            this.checkName()
          }
        })
      return isExist
    },
    async recycleName(str) {
      console.log('str', str)
      let lastName
      let strLen = str.length - 1
      let isExist
      console.log(lastName)
      for (let i = 0; i < strLen; i++) {
        lastName = str.slice(0, i + 1)
        isExist = await this.checkName(lastName)
        break
      }
      return isExist
    },
    //验证手机
    async checkPhone(type, value) {
      let ishas
      let existName
      if (type === 'phone') {
        existName = '手机号'
      } else {
        existName = '微信号'
      }
      await mango
        .getAjax('/v3/app/customer/check', {
          value: value,
          type: type,
          orgId: this.newCustomerInfo.orgId
        })
        .then(res => {
          if (res.status == 1) {
            MessageBox.alert(`该${existName}已存在`)
            ishas = true
          }
          if (res.status == 0) {
            ishas = false
            console.log(ishas)
          }
        })
        .catch(reject => {
          if (reject === 510) {
            this.checkPhone()
          }
        })
      return ishas
    },
    //
    saveData() {
      let formdata = new FormData()
      // let file = this.newCustomerInfo.dataFiles.getAll('record.dataFile')
      // for(let i = 0; i < file.length; i++){
      //   formdata.append('record.dataFile',file[i]);
      // }

      //let formdata = this.newCustomerInfo.dataFiles
      if (this.upLoadUrl) {
        let blob = this.changeFormData(this.upLoadUrl)
        formdata.append('dataFile', blob, Date.now() + '.jpg')
      }
      let obj = this.updateParams(this.newCustomerInfo)
      let arr = []
      for (var key in obj) {
        formdata.append(key, obj[key])
        arr.push(key)
      }
      indexModel
        .updateCustomer(formdata, arr, obj)
        .then(res => {
          if (res.status) {
            MessageBox.alert('更新成功').then(action => {
              this.editStatus = false
              this.getData()
            })
          } else {
            MessageBox.alert('更新失败')
          }
        })
        .catch(reject => {
          if (reject === 510) {
            this.saveData()
          }
        })
    },
    //base64转成formdata形式上传
    changeFormData(url) {
      let bytes = window.atob(url.split(',')[1])
      let temp = new ArrayBuffer(bytes.length)
      let ia = new Uint8Array(temp)
      for (var i = 0; i < bytes.length; i++) {
        ia[i] = bytes.charCodeAt(i)
      }
      //Blob对象
      let blob = new Blob([temp], { type: 'image/jpeg' }) //type为图片的格式
      return blob
      //FormData对象
      //let formdata = this.newCustomerInfo.dataFiles
      //formdata.append("dataFile", blob, Date.now() + ".jpg");
    },
    //获取参数
    updateParams(obj) {
      let tempObj = {}
      let temp = {
        phone: obj.phone ? obj.phone : '0',
        username: obj.username,
        sex: obj.sex,
        birthday: obj.birthday,
        age: obj.age,
        qq: obj.qq,
        weChat: obj.weChat,
        duty: obj.duty,
        remark: obj.remark !== '' ? mango.textEncode(obj.remark) : '',
        customerId: this.$route.query.id,
        orgId: obj.orgId || this.list.orgId
      }
      for (let key in temp) {
        if (temp[key] || temp[key] === 0) {
          tempObj[key] = temp[key]
        }
      }
      return tempObj
    },
    //切换顶部导航
    clickTab(index) {
      this.setDealTabStatus(
        mango.btnList(['订单信息', '客户信息', '意向信息'], index)
      )
    },
    goBack() {
      this.$router.back(-1)
    },
    //获取门店id
    getShopId(name) {
      if (this.shops && this.shops.length) {
        this.shops.forEach((item, index) => {
          if (item.name === name) {
            this.shopId = item.crmId
          }
        })
      }
      this.$set(this.newCustomerInfo, 'orgId', this.shopId)
      this.setNewCustomerInfo(this.newCustomerInfo)
    },
    //获取门店index
    getShopName(id) {
      if (this.shops && this.shops.length) {
        this.shops.forEach((item, index) => {
          if (item.crmId === id) {
            this.index = index
          }
        })
      }
      let shopsList = btnList(this.shops, this.index)
      this.initDescriptShopList(shopsList)
      this.getDescriptShopVal()
    }
  },
  //
  activated() {
    // isUseCache为false时才重新刷新获取数据
    this.$route.query.edit === 'no' ? (this.edit = 'no') : (this.edit = 'yes')
    if (!this.$route.meta.isUseCache) {
      this.editStatus = false
      let shops = localStorage.getItem('shops')
      this.shops = JSON.parse(shops)
      this.getStatus()
      this.getData()
      this.$route.meta.customerUseCache = false
    }
  },
  //
  beforeRouteLeave(to, from, next) {
    // /Customer
    if (to.name === 'address' || to.name === 'chooseShop') {
      this.$route.meta.isUseCache = true
    } else {
      this.$route.meta.isUseCache = false
      this.setUpLoadUrl('')
    }
    next()
  },
  beforeRouteEnter(to, from, next) {
    next(vm => {
      if (from.name === 'chooseShop') {
        let shops = localStorage.getItem('shops')
        vm.shops = JSON.parse(shops)
        if (vm.descriptShopVal) {
          vm.getShopId(vm.descriptShopVal)
        }
      }
    })
  }
}
</script>

