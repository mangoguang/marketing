          @click="clickTab(index)"
          :class="{'active' : dealTabStatus[index].status}">
        <span>{{item.name}}</span>
      </li>
    </ul>
    <!-- 订单信息-->
    <order-info v-show="dealTabStatus[0].status"
                :list='list' />
    <!-- 客户信息-->
    <div v-show="dealTabStatus[1].status">
      <customer-msg :isedit="edit"
                    :list="list"
                    :editMsg='editMsg'
                    v-if='!editStatus' />
      <div v-else>
        <newDescript :select='this.list.headPortrait? false : true'
                     :list='list'
                     :areaType='true'
                     :type='"descript"' />
        <btn @touchend.native="saveMsg()"
             :text="'保存资料'"
             class="theBtn"></btn>
      </div>
    </div>
    <!-- 意向信息-->
    <intentionMsg :isedit="edit"
                  v-show="dealTabStatus[2].status"
